def RunTest():
    manager = OpenRTM_aist.Manager.instance()
    comp = manager.getComponent("discord_botTest0")
    if comp is None:
        print('Component get failed.', file=sys.stderr)
        return False
    return comp.runTest()

def discord_botTestInit(manager):
    profile = OpenRTM_aist.Properties(defaults_str=discord_bottest_spec)
    manager.registerFactory(profile,
                            discord_botTest,
                            OpenRTM_aist.Delete)

def MyModuleInit(manager):
    discord_botTestInit(manager)
    discord_bot.discord_botInit(manager)

    # Create a component
    comp = manager.createComponent("discord_botTest")

def main():
    mgr = OpenRTM_aist.Manager.init(sys.argv)
    mgr.setModuleInitProc(MyModuleInit)
    mgr.activateManager()
    mgr.runManager(True)

    ret = RunTest()
    mgr.shutdown()

    if ret:
        sys.exit(0)
    else:
        sys.exit(1)

if __name__ == "__main__":
    main()

